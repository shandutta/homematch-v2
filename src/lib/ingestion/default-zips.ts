function key(city: string, state: string) {
  return `${city.trim().toUpperCase()}|${state.trim().toUpperCase()}`
}

export function defaultZipForCityState(
  city?: string,
  state?: string
): string | undefined {
  if (!city || !state) return undefined
  return CITY_ZIP_MAP[key(city, state)]
}
